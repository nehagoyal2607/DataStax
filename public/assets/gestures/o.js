import { Finger, FingerCurl, FingerDirection } from '../FingerDescription';
import GestureDescription from '../GestureDescription';
export const oDescription = new GestureDescription('o'); 
oDescription.addCurl(Finger.Thumb, FingerCurl.NoCurl, 1);
oDescription.addDirection(Finger.Thumb, FingerDirection.HorizontalRight, 1);
oDescription.addCurl(Finger.Index, FingerCurl.HalfCurl, 1);
oDescription.addDirection(Finger.Index, FingerDirection.DiagonalUpRight, 1);
oDescription.addCurl(Finger.Middle, FingerCurl.HalfCurl, 1);
oDescription.addDirection(Finger.Middle, FingerDirection.DiagonalUpRight, 1);
oDescription.addCurl(Finger.Ring, FingerCurl.HalfCurl, 1);
oDescription.addDirection(Finger.Ring, FingerDirection.DiagonalUpRight, 1);
oDescription.addCurl(Finger.Pinky, FingerCurl.HalfCurl, 1);
oDescription.addDirection(Finger.Pinky, FingerDirection.DiagonalUpRight, 1);
oDescription.addDirection(Finger.Thumb, FingerDirection.HorizontalLeft, 1);
oDescription.addDirection(Finger.Index, FingerDirection.DiagonalUpLeft, 1);
oDescription.addDirection(Finger.Middle, FingerDirection.DiagonalUpLeft, 1);
oDescription.addDirection(Finger.Ring, FingerDirection.DiagonalUpLeft, 1);
oDescription.addDirection(Finger.Pinky, FingerDirection.DiagonalUpLeft, 1);
export default oDescription;