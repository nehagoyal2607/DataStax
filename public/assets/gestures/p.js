pDescription.addDirection(Finger.Thumb, FingerDirection.DiagonalUpRight, 1);
pDescription.addCurl(Finger.Index, FingerCurl.NoCurl, 1);
pDescription.addDirection(Finger.Index, FingerDirection.HorizontalRight, 1);
