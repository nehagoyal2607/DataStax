pDescription.addDirection(Finger.Middle, FingerDirection.DiagonalUpRight, 1);
pDescription.addCurl(Finger.Ring, FingerCurl.FullCurl, 1);
pDescription.addDirection(Finger.Ring, FingerDirection.HorizontalRight, 1);
pDescription.addCurl(Finger.Pinky, FingerCurl.FullCurl, 1);
pDescription.addDirection(Finger.Pinky, FingerDirection.HorizontalRight, 1);
pDescription.addDirection(Finger.Thumb, FingerDirection.DiagonalUpLeft, 1);
pDescription.addDirection(Finger.Index, FingerDirection.HorizontalLeft, 1);
pDescription.addDirection(Finger.Middle, FingerDirection.DiagonalUpLeft, 1);
pDescription.addDirection(Finger.Ring, FingerDirection.HorizontalLeft, 1);
pDescription.addDirection(Finger.Pinky, FingerDirection.HorizontalLeft, 1);
export default pDescription;