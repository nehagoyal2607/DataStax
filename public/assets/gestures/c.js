import { Finger, FingerCurl, FingerDirection } from '../FingerDescription';
import GestureDescription from '../GestureDescription';
export const cDescription = new GestureDescription('c'); 
cDescription.addCurl(Finger.Thumb, FingerCurl.NoCurl, 1);
cDescription.addDirection(Finger.Thumb, FingerDirection.HorizontalRight, 1);
cDescription.addCurl(Finger.Index, FingerCurl.NoCurl, 1);
cDescription.addDirection(Finger.Index, FingerDirection.DiagonalUpRight, 1);
cDescription.addCurl(Finger.Middle, FingerCurl.HalfCurl, 1);
cDescription.addDirection(Finger.Middle, FingerDirection.DiagonalUpRight, 1);
cDescription.addCurl(Finger.Ring, FingerCurl.HalfCurl, 1);
cDescription.addDirection(Finger.Ring, FingerDirection.DiagonalUpRight, 1);
cDescription.addCurl(Finger.Pinky, FingerCurl.HalfCurl, 1);
cDescription.addDirection(Finger.Pinky, FingerDirection.DiagonalUpRight, 1);
cDescription.addDirection(Finger.Thumb, FingerDirection.HorizontalLeft, 1);
cDescription.addDirection(Finger.Index, FingerDirection.DiagonalUpLeft, 1);
cDescription.addDirection(Finger.Middle, FingerDirection.DiagonalUpLeft, 1);
cDescription.addDirection(Finger.Ring, FingerDirection.DiagonalUpLeft, 1);
cDescription.addDirection(Finger.Pinky, FingerDirection.DiagonalUpLeft, 1);
export default cDescription;